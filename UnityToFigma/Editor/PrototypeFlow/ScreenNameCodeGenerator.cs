using System.Collections.Generic;
using System.IO;
using UnityEditor;
using UnityEngine;
using UnityToFigma.Editor.Import;
using UnityToFigma.Editor.Settings;
using UnityToFigma.Editor.Utils;

namespace UnityToFigma.Editor.PrototypeFlow
{
    /// <summary>
    /// Generates CS file with flowScreen names as constants for reference by functionality
    /// </summary>
    public static class ScreenNameCodeGenerator
    {
        public static void WriteScreenNamesCodeFile(List<GameObject> screenObjects, UnityToFigmaSettings settings)
        {
            var outputPath = new FigmaImportPathResolver(settings).GetScreenNamesCodeFilePath();

            var content = @"
using System;
 
public class FigmaScreenNames {

    // Automatically generated by UnityToFigma
          
";
            foreach (var screen in screenObjects)
            {
                var screenName = screen.name;
                content += $"\tpublic const string {GetCodeSafeNameForScreen(screenName)}=\"{screenName}\";\n";
            }
            
            content+=@"
}
";
                
                
            File.WriteAllText(outputPath, content);
            // Reimport
            AssetDatabase.ImportAsset(outputPath);
        }

        public static string GetCodeSafeNameForScreen(string screenName)
        {
            var safeTitle = FigmaPaths.MakeValidFileName(screenName.ToUpper());
            safeTitle = safeTitle.Replace(" ", "_");
            return safeTitle;
        }
    }
}